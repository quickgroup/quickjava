package org.quickjava.common.enums;

public enum DatetimeCurrType {
    // 现在年份（如：2023
    YEAR,
    // 现在月份（如：03
    MONTH,
    // 现在月日（如：15
    DAY,
    // 现在年月（如：2023-03
    YEAR_MONTH,
    // 现在日期（如：2023-03-15
    DATE,
    // 现在小时（如：15
    HOUR,
    // 现在分钟（如：58
    MINUTE,
    // 现在秒（如：59
    SECOND,
    // 现在整点小时分钟（如：15:58
    HOUR_MINUTE,
    // 现在时间（如：15:58:59
    TIME,
    // 现在日期时间（如：2023-03-15 15:58:59
    DATETIME,
    ;
}
